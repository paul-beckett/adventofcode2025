func TestPart1(t *testing.T) {
	var testCases = map[string]struct {
		data string
		want int
	}{
		"example1": {
			data: `162,817,812
57,618,57
906,360,560
592,479,940
352,342,300
466,668,158
542,29,236
431,825,988
739,650,466
52,470,668
216,146,977
819,987,18
117,168,530
805,96,715
346,949,466
970,615,88
941,993,340
862,61,35
984,92,344
425,690,689`,
			want: 40,
		},
	}

	for name, tc := range testCases {
		t.Run(name, func(t *testing.T) {
			//given
			day := day08.NewDay08(strings.Split(tc.data, "\n"))

			//when
			got, err := day.Part1WithLimit(10)

			//then
			assert.NoError(t, err)
			assert.Equal(t, tc.want, got)
		})
	}
}

